#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char*new;
	int i;
	int size;

	size = 0;
	size = ft_strlen((char *)s1);
	new = malloc(sizeof(char) * (size + 1));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
